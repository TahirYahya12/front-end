                <h2>Asal-usul Tanaman Jagung</h2>
                <hr>
                <hr>
                <p>Banyak yang memperkirakan bahwa asal-usul jagung berasal dari Meksiko, Amerika tengah. Hal ini dibuktikan dengan petunjuk arkeologi 7000 tahun yang lalu, dimana terdapat budidaya jagung secara primitif tepatnya di Selatan Meksiko.</p>
                <p>
                    Bahkan 1000 tahun yang lalu bangsa Olmek dan Maya, Amerika Tengah, sudah menanam jagung dengan berbagai teknik pengolahannya.
                </p>
                <p>

                    Pada 7000 tahun lalu, terdapat teknologi yang dibawa ke Amerika Selatan tepatnya di Ekuador. Bahkan hampir seluruh benua Amerika mengenal penanaman jagung.
                </p>

                <p>
                    Ada banyak tongkol jagung yang tersisa ditemukan di sekitar gua-gua, seperti Gua Guila Naquitz, Tehuacan, Puebla, Meksiko, yang usianya beribu-ribu tahun. Pada akhir abad ke-15, penyebaran tanaman jagung sudah sampai ke wilayah Asia dan Eropa.

                </p>
                <p>

                    Persebaran jagung di Indonesia sendiri berasal dari penjelajah Portugis yang membawanya di abad ke-16. Jagung di Indonesia memiliki nama yang lokal yang berbeda sesuai dengan daerah tersebut.</p><br><br>
                </p>
                <h2>Budidaya</h2>
                <hr>
                <hr>
                <h5>Teknis Penanaman Jagung</h5>
                <p>keberhasilan budidaya jagung juga ditentukan oleh teknis, budidaya, hingga proses panen. kemudian keberadaan mikroba didalam tanahnya juga menjadi salah satu factor keberhasilan menanam jagung.
                    Untuk lebih jelasnya berikut uraian teknik budidaya tanaman jagung yang baik dan benar agar menghasilkan produksi jagung yang tinggi.</p>
                <h5>Pemilihan Benih Jagung</h5>
                <p>Pilihlah benih jagung hibrida yang telah bersertifikat. Pada setiap provinsi di Indonesia telah tersedia benih jagung jenis unggul ini. Biasanya benih jagung telah diberi perlakuan seed treatment, yaitu dengan melapisi fungisida pada benih yang
                    berfungsi agar tanaman terlindung dari berbagai serangan penyakit dan mempermudah syarat tumbuh tanaman jagung.</p>
                <h5>Syarat Tumbuh Tanaman Jagung</h5>
                <p>Pada umumnya tanaman jagung dapat tumbuh di berbagai kondisi lingkungan. Namun untuk hasil yang maksimum,
                    ada beberapa syarat tumbuh tanaman jagung. Berikut adalah syarat tumbuh tanaman jagung:</p>
                <p>1.Iklim</p>
                <p>2.Media Tanah</p>
                <p>3.Ketinggian</p>
                <h5>Jarak Tanaman Jagung</h5>
                <p>Pada kondisi tanah yang berjenis tanah becek, sebaiknya dibuatkan bedengan/guludhan agar benih tidak tergenang air dan tidak busuk. Sehingga benih akan tumbuh cepat dan maksimal. Lebar bedengan adalah 100 cm dan jarak antar bedengan adalah 50 cm.
                    Sedangkan jarak didalam barisnya adalah 20-25 cm, sehingga jarak tanam jagung, baik menggunakan bedengan ataupun yang tidak mengunakan bedengan adalah 75cm x 25cm atau 75cm x 20cm
                    Setelah itu buatlah lubang tanam dengan cara tugal sedalam 5-10 cm kemudian masukkan benih jagung dan tutup dengan bokashi. Setelah itu, semprot dengan POC GDM pada bekas lubang tanam. Ini berfungsi untuk mempercepat pertumbuhan tanaman dan melindungi tanaman dari serangan penyakit.</p>
                <h5>Pemupukan Tanaman Jagung</h5>
                <p>Setelah tanaman jagung tumbuh ,lakukan penyemprotan dengan pupuk organik cair GDM spesialis tanaman pangan sayur setiap 10 hari sekali dengan dosis 2 gelas air mineral pada tanaman dan daerah perakaran untuk memaksimalkan pertumbuhan tanaman. Sedangkan pupuk kimia diberikan pada saat tanaman jagung berumur 10,
                    21 dan 50 HST dengan memberikan 400 Kg pupuk NPK dan 200-300 pupuk kandungan Nitrogen.</p>
                <h5>Pemeliharan Tanaman Jagung</h5>
                <p>Pemeliharaan tanaman jagung dapat dilakukan dalam berbagai tahap, yaitu:</p>
                <p> 1. Penjarangan dan Penyulaman
                    Proses ini dilakukan pada saat tanam ada dua atau lebih benih jagung yang tertanam, sehingga tumbuh dua atau lebih tanaman jagung. Oleh sebab itu, harus dilakukan penjarangan.Proses penyulaman tanaman jagung dilakukan apabila ada tanaman yang mati dengan mengantikan tanaman baru.
                </p>
                <p>

                    2. Penyiangan
                    Melakukan proses pembersihan tanaman yang pengganggu di sekitar tanaman jagung, seperti rumput, krokot, keladi dan tanaman pengganggu lainnya.
                </p>
                <p>

                    3. Pembumbunan
                    Pelaksanaannya dapat dilakukan secara bersamaan saat proses penyiangan dengan tujuan memperkuat akar tanaman serta membantu mempercepat pertumbuhan.</p>
