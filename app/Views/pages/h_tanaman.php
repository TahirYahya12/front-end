                <h5>Serat</h5>
                <p>Jagung mengandung serat yang cukup banyak. Jumlah serat dalam jagung setara dengan 42 hingga 64 nilai harian. Dikutip dari Healthline, serat yang dominan pada jagung adalah serat yang tidak larut, seperti hemiselulosa, selulosa, dan lignin.</p>
                <h5>Lemak</h5>
                <p>Jagung secara alami adalah bahan pangan yang rendah lemak. Mayoritas lemak dalam jagung berasal dari lemak tak jenuh tunggal dan ganda yang baik bagi kesehatan jantung.</p>
                <h5>Protein</h5>
                <p>Jagung merupakan sumber protein yang baik. Tergantung pada varietasnya, kandungan protein jagung berkisar antara 10 hingga 15 persen kebutuhan harian. Protein paling melimpah dalam jagung dikenal sebagai zeins. Meski melimpah, tapi secara keseluruhan, kualitas protein zeins buruk karena kekurangan beberapa asam amino esensial.</p>
                <h5>Vitamin dan Mineral</h5>
                <p>Jagung mengandung cukup banyak vitamin dan mineral, bergantung pada varietasnya. Secara umum, beberapa unsur penting yang terdapat dalam jagung adalah: Mangan adalah unsur yang ditemukan dalam jumlah tinggi dalam biji-bijian, kacang-kacangan, buah, dan sayur. Fosfor ditemukan dalam jumlah yang cukup baik dalam jagung dan berperan penting dalam pertumbuhan dan pemeliharaan tubuh. Magnesium adalah mineral penting yang dapat menurunkan risiko penyakit kronis. Seng yang cukup banyak ditemui dalam jagung. Tembaga adalah antioksidan yang memiliki efek baik untuk kesehatan jantung. Folat atau vitamin B9 yang penting bagi ibu hamil Asam pantotenat atau vitamin B5 yang ditemukan dalam hampir semua jenis makanan. Vitamin B6 yang baik untuk berbagai fungsi tubuh. Niasin atau vitamin B3 Kalium penting bagi kontrol tekanan darah dan kesehatan jantung</p>
                <h5>Senyawa Lain</h5>
                <p>Jagung juga mengandung sejumlah senyawa tanaman bioaktif yang baik untuk kesehatan. Beberapa di antaranya: Asam ferulic adalah salah satu antioksidan polifenol utama dalam jagung. Antosianin adalah keluarga pigmen antioksidan yang bertanggung jawab atas warna jagung. Zeaxanthin adalah salah satu karotenoid tumbuhan paling umum yang dikaitkan dengan peningkatan kesehatan mata. Lutein adalah salah satu karotenoid utama dalam jagung yang berfungsi sebagai antioksidan, melindungi mata Anda dari kerusakan oksidatif yang dihasilkan oleh cahaya biru. Asam fitat adalah antioksidan yang dapat mempengaruhi penyerapan mineral makanan Anda, seperti seng dan zat besi.</p><br><br>
