                <h2>Morfologi</h2>
                <hr>
                <hr>
                <h5>1. Batang Tanaman Jagung</h5>
                <p>Batang yang dimiliki oleh tanaman jagung ialah mudah terlihat, tegak, serta beruas. Ruas dibungkus dengan pelepah daun dari buku. Batang tanaman jagung tidak mengandung lignin yang banyak.</p>
                <h5>2. Akar Tanaman Jagung</h5>
                <p>Perakaran tanaman ini ialah akar serabut kedalaman 8 meter, tetapi sebagian besar terdapat pada kedalaman 2 meter
                    Ketika dewasa, tanaman jagung menumbuhkan akar adventif yang berasal dari buku batang di bagian bawah tanaman. Agar tanaman dapat tegak lurus.</p>
                <h5>3. Tongkol Tanaman Jagung</h5>
                <p>Keberadaan tongkol jagung yaitu di antara batang daun dan pelepah daun serta tumbuh dari buku. pada umumnya tanaman jagung bisa menghasilkan 1 tongkol produktif saja. Penyerbukan bunga jantan dilakukan sebanyak 2-5 hari lebih dulu dibanding bunga betina.</p>
                <h5>4. Bunga Tanaman Jagung</h5>
                <p>Tanaman jagung memiliki bunga betina dan jantan yang terpisah di satu tanaman. Bunga jantan tumbuh di puncak tanaman, berupa inflorescence atau karangan bunga. Bunga jagung memiliki serbuk sari bewarna kuning dan beraroma khas.</p>
                <h5>5. Daun Tanaman Jagung</h5>
                <p>Tanaman jagung memiliki daun yang berbentuk panjang, warna hijau muda ketika tanaman masih muda, warna hijau tua ketika tanaman dewasa, dan berwarna kuning saat tanaman tua.

                    Daun tanaman jagung mempunyai tulang daun yang sejajar ibu tulang daun. Diantara pelepah daun serta tangkai daun ada ligula. Permukaan daun ada yang licin dan berambut. Daun jagung mempunyai stomata berbentuk halter.

                    Dan setiap stomata tersebut dikelilingi oleh sel epidermis seperti kipas. Pada struktur tersebut sangat penting guna menanggapi defisit air dalam sel daun jagung.</p><br>
