                <h2>Fase Pertumbuhan</h2>
                <hr>
                <hr>
                <h5>1. Fase Kecambah</h5>
                <p>Fase ini terjadi ketika benih jagung mulai menyerap air sampai benih jagung membengkak.

                    Kecambah jagung akan muncul ke permukaan tanah setelah 4 sampai 5 hari setelah ditanam.

                    Jika kondisinya sedang kering, kecambah akan lebih lama tumbuhnya, sekitar 2 minggu atau lebih.</p>
                <h5>2. Fase V3-V5 (Jumlah Daun Terbuka Sempurna 3-5)</h5>
                <p>Fase ini terjadi ketika tanaman jagung sudah berumur sekitar 10 sampai 18 hari dihitung setelah berkecambah.

                    Akar seminal berhenti tumbuh dan akar nodul mulai bertumbuh. Akar seminal yaitu akar yang tumbuh ketika benih jagung berkecambah.

                    Sedangkan akar nodul adalah akar yang terus tumbuh di dalam tanah untuk penyerapan air dan nutrisi.</p>
                <h5>3. Fase V6-V10 (Jumlah Daun Terbuka Sempurna 6-10)</h5>
                <p>Fase ini terjadi ketika tanaman jagung sudah berumur sekitar 18 sampai 35 hari dihitung setelah berkecambah.

                    Akar sudah tumbuh dengan cepat, bunga jantan juga mulai tumbuh dan perkembangan tongkol jagung baru dimulai.</p>
                <h5>4. Fase V11-Vn (Jumlah Daun Terbuka Sempurna 11-18)</h5>
                <p>Fase ini terjadi ketika tanaman jagung sudah berumur sekitar 33 sampai 50 hari dihitung setelah berkecambah.

                    Tanaman jagung pada fase ini berkembang dengan cepat, jangan sampai kekurangan nutrisi dan air.

                    Karena hal itu akan memengaruhi jumlah biji tiap tongkol dan ukuran tongkol akan mengecil.</p>
                <h5>5. Fase VT (Berbunga Jantan)</h5>
                <p>Fase ini terjadi ketika tanaman jagung sudah berumur sekitar 45 sampai 52 hari.

                    Tandanya dimulai dengan cabang terakhir bunga jantan sebelum bunga betina muncul.

                    Tanaman jagung juga sudah mulai menyebarkan serbuk sari.
                </p>
                <h5>6. Fase R1 (Silking)</h5>
                <p>Fase ini diawali dengan munculnya rambut dari dalam tongkol, fase ini terjadi setelah 2 atau 3 hari dari fase VT.

                    Serbuk sari akan jatuh di rambut tongkol yang masih segar dan membutuhkan waktu sehari untuk membentuk bakal biji. Rambut tongkol juga akan semakin panjang tiap harinya.
                </p>
                <h5>7. Fase R2 (Blister)</h5>
                <p>Fase ini diawali sekitar 10 sampai 14 hari setelah fase R1, ditandai dengan rambut tongkol yang kering dan berwarna gelap.

                    Biji jagung sudah mulai nampak dan berwarna putih, tongkol juga semakin sempurna.</p>
                <h5>8. Fase R3 (Masak Susu)</h5>
                <p>Fase ini terjadi sekitar 18 sampai 22 hari setelah fase R1. Biji jagung sudah mulai sedikit menguning.

                    Pada fase ini tanaman jagung jangan sampai kekeringan karena akan menurunkan ukuran dan jumlah biji.</p>
                <h5>9. Fase R4 (Dough)</h5>
                <p>Fase ini terjadi sekitar 24 sampai 28 hari setelah fase R1. Biji jagung mulai mengering dan setengah lunak, belum mengeras sepenuhnya.
                </p>
                <h5>10. Fase R5 (Pengerasan Biji)</h5>
                <p>Fase ini terjadi sekitar 35 sampai 42 hari setelah fase R1. Biji jagung sudah terbentuk dengan sempurna dan adar air dalam jagung juga menurun setengahnya.</p>
                <h5>11. Fase R6 (Masak)</h5>
                <p>Fase ini terjadi sekitar 55 sampai 65 hari setelah fase R1. Pada fase ini, biji jagung yang terbentuk sudah semakin turun dkadar airnya dan sudah kering dan benar-benar mengeras.

                    Nah, itulah fase-fase pertumbuhan tanaman jagung. Dimulai dari tunas sampai biji jagung siap dipanen.</p><br><br>
