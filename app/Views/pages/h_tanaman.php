                <h5>Karbohidrat</h5>
                <p>Kandungan utama dalam kalori jagung adalah karbohidrat. Karbohidrat utama dari bahan pangan ini adalah pati dan gula. Sebagian besar gula pada jagung adalah sukrosa. Meskipun ada gula dalam jagung manis, sumber karbohidrat yang satu ini memiliki indeks glikemik (GI) rendah. Dikutip dari Verywell Fit, indeks glikemik jagung berkisar antara 56 hingga 69,2. GI adalah ukuran seberapa cepat karbohidrat dicerna.
                    Makanan yang berperingkat tinggi pada indeks ini dapat menyebabkan lonjakan gula darah yang tidak sehat.</p>
